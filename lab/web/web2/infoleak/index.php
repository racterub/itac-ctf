      <h1>關於 ITAC</h1>
      <p>
        ITAC&nbsp;&nbsp;創立的初衷&nbsp;&nbsp;—&nbsp;&nbsp;「有些事現在不做，一輩子都不會做」。<br />
        2014&nbsp;&nbsp;年的暑假尾聲，正當其他同學們在各地玩得不亦樂乎時，一群熱愛資訊的元智人默默開始聚集，
        秉持著「為什麼元智沒有資訊社？」、「那就創一個吧？」的想法，創立了元智第一個資訊性社團&nbsp;&nbsp;—&nbsp;&nbsp;ITAC。
      </p>
      <p>
        ITAC&nbsp;&nbsp;全名為&nbsp;&nbsp;Information Technology Association Club，是由一群熱愛程式的元智學生創立的社團。<br />
        ITAC&nbsp;&nbsp;現階段的成員橫跨大一到大四，成員擅長領域包括&nbsp;Android、iOS App&nbsp;開發，
        HTML、CSS、JavaScript、PHP&nbsp;網站設計，
        MySQL&nbsp;資料庫管理，Python，資訊安全等等，
        更有多位成員早已在業界工作或是代表學校參加比賽，成員們也期望未來能夠分享給喜歡資訊的同學們。
      </p>
      <p>
        如果對於寫程式只為應付作業的生活感到厭倦，或是想嘗試課外之餘接案賺取零用的同學，給自己一個機會，
        一起學習「學校不教的事」，讓自己擁有更多能力去改變自己、改變世界。
      </p>

    </content>
  </section>
