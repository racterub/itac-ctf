      <h1>社團活動介紹</h1>
      <p>
        我們在上學年前期開設了許多基礎課程，如：Python、PHP、Git&nbsp;版本控制新手村......等等。<br />
        除了基礎課程、專案製作，ITAC&nbsp;定期也會揪團參加各式社群活動，如：SITCON、COSCUP、HITCON，
        帶領社員與國內外同樣熱衷資訊技術的人們交流，增廣見聞、看看其他人都在做些什麼，也多認識領域中的其他強者。
      </p>
      <p>
        在這個新學期，ITAC&nbsp;要招募與我們有共同目標的新血。
      </p>
      <p>
        下學期開始，我們非常榮幸地邀請到業界著名的&nbsp;TonyQ&nbsp;作為我們的社團指導老師，
        我們將會舉辦演講、開設新手教學課程，當然少不了專案製作，也會舉辦社遊，增進學員間的感情。
        由於學員們的課表不一，可能將在之後重新調整社課時間。
      </p>
      <p>
        想要尋找程式路上的夥伴嗎？<br />
        想在高手雲集的地方和大家一起切磋技術、交流進步嗎？<br />
        想跨出你遲遲不敢跨出的第一步嗎？<br />
        想和大家一起打怪練功嗎？<br />
        無論你是新手、老手，想要專攻什麼樣的領域，歡迎正式加入&nbsp;ITAC。
      </p>
